function getputdata(hObject, eventdata, handles)
x1=eval(get(handles.x,'String'));
y1=eval(get(handles.y,'String'));
[x y]=meshgrid(x1,y1);
if get(handles.popupmenu1,'Value')<2
    return;
end
handles.zdata=eval(handles.selected);
handles.xdata=x;
handles.ydata=y;
guidata(hObject,handles);
meshdata(handles.axes2,eventdata,handles);

function meshdata(hObject,eventdata,handles)
mesh(handles.xdata,handles.ydata,handles.zdata);
grid;


